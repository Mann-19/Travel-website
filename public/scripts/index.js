// General
const cardContainer1 = document.getElementById("card-container-1");
const cardContainer2 = document.getElementById("card-container-2");

const body = document.querySelector("body");
const sidebar = document.getElementById("sidebar");
const sidebarOverlay = document.getElementById("sidebar-overlay");
const closeBtn = document.getElementById("close-btn");

// Function to create each card dynamically
function createCard(cardData, cardContainer) {
  const card = document.createElement("div");
  card.classList.add(
    "relative",
    "w-1/4",
    "min-w-[23%]",
    "h-52",
    "rounded-lg",
    "overflow-hidden",
    "cursor-pointer"
  );

  card.innerHTML = `
    <div class="h-full bg-cover bg-center relative" style="background-image: url('${cardData.imgUrl}');">
        <div class="absolute inset-0 bg-black opacity-30"></div>
        <div class="absolute inset-0 flex justify-center items-end text-light p-4">
            <h2 class="text-2xl font-bold">${cardData.name}</h2>
        </div>
    </div>
    `;

  cardContainer.appendChild(card);

  card.addEventListener("click", () => {
    populateSidebarData(cardData.sidebar);
    body.classList.add("overflow-hidden");
    sidebarOverlay.classList.toggle("hidden");
    sidebar.classList.toggle("w-0");
    sidebar.classList.toggle("w-[95%]");
  });
}

closeBtn.addEventListener("click", () => {
  body.classList.remove("overflow-hidden");
  sidebarOverlay.classList.toggle("hidden");
  sidebar.classList.toggle("w-[95%]");
  sidebar.classList.toggle("w-0");
});

locationCards1.forEach((cardData) => {
  createCard(cardData, cardContainer1);
});
locationCards2.forEach((cardData) => {
  createCard(cardData, cardContainer2);
});

// Function to populate data into sidebar dynamically
function populateSidebarData(sidebarData) {
  document.getElementById("location-name").textContent =
    sidebarData.locationName;
  document.getElementById("sidebar-img").src = sidebarData.img;
  document.getElementById("sub-location-name").textContent =
    sidebarData.locationName;
  document.getElementById("sidebar-info").textContent = sidebarData.info;

  const sideBarCardContainer = document.getElementById(
    "sidebar-card-container"
  );
  sideBarCardContainer.innerHTML = "";

  sidebarData.todoCards.forEach((todoCard) => {
    const card = document.createElement("div");
    card.classList.add("flex", "flex-col", "items-center", "mb-4");
    card.innerHTML = `
        <div class="w-full mx-auto bg-lightest rounded-xl overflow-hidden my-8">
            <img class="w-full h-[50%] object-cover" src="${todoCard.img}" alt="Card Image">
            <div class="p-4">
                <h2 class="text-base font-[500] text-dark">${todoCard.activity}</h2>
                <span class="text-mid text-sm">Some content goes here...</span>
            </div>
            <div class="flex justify-between items-center p-4">
                <div>
                    <p class="text-sm font-[400] text-dark">&#8377;${todoCard.price}</p>
                </div>
                <button class="bg-brand text-darkest p-2 rounded-lg text-sm">Book</button>
            </div>
        </div>
    `;

    populateTravelAdvice(sidebarData.travelAdvice);
    sideBarCardContainer.appendChild(card);
  });
}

// Funtion to populate data into travel-advice section dynamically
function populateTravelAdvice(travelData) {
  const visitTimeContent = document.getElementById("visit-time-content");
  visitTimeContent.textContent = travelData.bestTimeToVisit;

  const localCustomsContent = document.getElementById("local-customs-content");
  localCustomsContent.innerHTML = "";

  travelData.localCustoms.forEach((data) => {
    const element = document.createElement("div");
    element.innerHTML = `<div class="mx-20 mt-4">
    <h3 class="text-lg font-[700] text-dark">${data.heading}</h3>
    <p class="text-base text-darkest">${data.content}</p>
    </div>`;

    localCustomsContent.appendChild(element);
  });
}

